public class Point {
    //Point parameters. x and y coordinates.
    public int x = 0;
    public int y = 0;

    /**
     * Constructor for declaring x and y point.
     * @param a - x coordinate.
     * @param b - y coordinate.
     */
    public Point(int a, int b) {
        x = a;
        y = b;
    }
}
